/* Questa SP ritorna True se c'è almeno una chiamata/appuntamento urgente per l'impianto specificato */

CREATE PROCEDURE IMPIANTI_SCADSOFF_EXISTS (IN_CODICEPRATICA INTEGER, IN_DATAPRATICA TIMESTAMP)
  RETURNS (OUTVAL CHAR(1))
AS
BEGIN

  IF (EXISTS(SELECT NULL FROM SCADENZ S WHERE S.PRATICA=:IN_CODICEPRATICA AND S.DATAPRATICA1=:IN_DATAPRATICA AND S.DATASCADENZA < CAST('TODAY' AS TIMESTAMP) AND IMPORTOPAGATO < IMPORTO)) 
    THEN OUTVAL = 'T';
    ELSE OUTVAL = 'F';

  SUSPEND;
  EXIT;

END^

