SET TERM ^ ;

CREATE TRIGGER RAPGIORN_AFTERDELETE FOR RAPGIORN
ACTIVE AFTER DELETE POSITION 0
AS
BEGIN

  /* Elimina gli allegati relativi al documento eliminato */
  DELETE FROM ALLEGATI A  
  WHERE A.TIPODOC = 'Rapp.giorn.'
    AND A.REGDOC  = OLD.REGISTRO
    AND A.NUMDOC  = OLD.NUMDOC
    AND A.DATADOC = OLD.DATADOC;

END^

SET TERM ; ^
