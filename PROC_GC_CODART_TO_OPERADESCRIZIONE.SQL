CREATE PROCEDURE GC_CODART_TO_OPERADESCRIZIONE (IN_COD_ART VARCHAR(25))
  RETURNS (OUTVAR VARCHAR(45))
AS
  DECLARE C CURSOR FOR (SELECT FIRST 1 DESCRIZIONEOPERA, DESCRIZIONEOPERA_2, DESCRIZIONEOPERA_3, DESCRIZIONEOPERA_4, DESCRIZIONEOPERA_5 FROM PROGRESS);
  DECLARE VARIABLE loc_DescOpera1 VARCHAR(45);
  DECLARE VARIABLE loc_DescOpera2 VARCHAR(45);
  DECLARE VARIABLE loc_DescOpera3 VARCHAR(45);
  DECLARE VARIABLE loc_DescOpera4 VARCHAR(45);
  DECLARE VARIABLE loc_DescOpera5 VARCHAR(45);
  DECLARE VARIABLE loc_OperaIndex INTEGER;
BEGIN

  IF (:IN_COD_ART IS NULL) THEN BEGIN
    OUTVAR='';
    SUSPEND;
    EXIT;
  END

  EXECUTE PROCEDURE GC_CODART_TO_OPERAINDEX :IN_COD_ART RETURNING_VALUES :loc_OperaIndex;

  OPEN C;
  FETCH C INTO :loc_DescOpera1, :loc_DescOpera2, :loc_DescOpera3, :loc_DescOpera4, :loc_DescOpera5;
  CLOSE C;

  IF (:loc_OperaIndex = 0) THEN BEGIN
    OUTVAR=:loc_DescOpera1;
    SUSPEND;
    EXIT;
  END

  IF (:loc_OperaIndex = 1) THEN BEGIN
    OUTVAR=:loc_DescOpera2;
    SUSPEND;
    EXIT;
  END

  IF (:loc_OperaIndex = 2) THEN BEGIN
    OUTVAR=:loc_DescOpera3;
    SUSPEND;
    EXIT;
  END

  IF (:loc_OperaIndex = 3) THEN BEGIN
    OUTVAR=:loc_DescOpera4;
    SUSPEND;
    EXIT;
  END

  IF (:loc_OperaIndex = 4) THEN BEGIN
    OUTVAR=:loc_DescOpera5;
    SUSPEND;
    EXIT;
  END

  OUTVAR='';
  SUSPEND;
  EXIT;

END