/* Questa SP ritorna True se c'è almeno una chiamata/appuntamento urgente per l'impianto specificato */

CREATE PROCEDURE IMPIANTI_CORRISPNOPAG_EXISTS (IN_CODICEPRATICA INTEGER, IN_DATAPRATICA TIMESTAMP)
  RETURNS (OUTVAL CHAR(1))
AS
BEGIN

  IF (EXISTS(SELECT NULL FROM PRVORDCL T WHERE T.PRATICA=:IN_CODICEPRATICA AND T.DATAPRATICA1=:IN_DATAPRATICA AND T.RIFDOC_CORRISPNOPAG='T'))
    THEN OUTVAL = 'T';
    ELSE OUTVAL = 'F';

  SUSPEND;
  EXIT;

END^

