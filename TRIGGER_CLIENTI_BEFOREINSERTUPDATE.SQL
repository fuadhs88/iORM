CREATE TRIGGER CLIENTI_BEFOREINSERTUPDATE FOR CLIENTI
ACTIVE BEFORE INSERT OR UPDATE
AS
BEGIN

  IF (COALESCE(NEW.TIPOPERSONA,'') = 'F'
    AND (   TRIM(COALESCE(NEW.COGNOME,'')) <> ''   OR   TRIM(COALESCE(NEW.NOME,'')) <> ''   )   )
  THEN NEW.RAGIONESOCIALE = SUBSTRING(TRIM(   TRIM(COALESCE(NEW.COGNOME,'')) || ' ' || TRIM(COALESCE(NEW.NOME,''))   ) FROM 1 FOR 60); 

END
