CREATE VIEW STRADARIO_LIST_VIEW (

   CODICEISTATCOMUNECOMPLETO
  ,DENOMINAZIONESTRADACOMPLETA
  ,DENOMINAZIONECOMUNE
  ,SIGLAPROVINCIA
  ,DENOMINAZIONEPROVINCIA
  ,DENOMINAZIONEREGIONE
  ,CAP

) AS

  SELECT

   STR.CODICEISTATCOMUNECOMPLETO 
  ,STR.DENOMINAZIONESTRADACOMPLETA
  ,COM.DENOMINAZIONECOMUNE
  ,PRV.SIGLAPROVINCIA
  ,PRV.DENOMINAZIONEPROVINCIA
  ,PRV.DENOMINAZIONEREGIONE
  ,CAP.CAP

  FROM STRADARIO_COMUNI COM
  LEFT JOIN STRADARIO_STRADE STR ON (STR.CODICEISTATCOMUNECOMPLETO=COM.CODICEISTATCOMUNECOMPLETO)
  LEFT JOIN STRADARIO_PROVINCE PRV ON (PRV.CODICEISTATPROVINCIA=COM.CODICEISTATPROVINCIA)
  LEFT JOIN STRADARIO_CAP CAP ON (CAP.CODICEISTATCOMUNECOMPLETO=COM.CODICEISTATCOMUNECOMPLETO)

  WHERE COM.CODICEISTATCOMUNECOMPLETO <> ''
;
