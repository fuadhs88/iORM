/* Questa SP ritorna True se c'è almeno una chiamata/appuntamento urgente per l'impianto specificato */

CREATE PROCEDURE IMPIANTI_URGENTI_EXISTS (IN_CODICEPRATICA INTEGER, IN_DATAPRATICA TIMESTAMP)
  RETURNS (OUTVAL CHAR(1))
AS
BEGIN

  IF (EXISTS(SELECT I.ID FROM IMPEGNI I WHERE I.PRATICA=:IN_CODICEPRATICA AND I.DATAPRATICA1=:IN_DATAPRATICA AND I.TIPOIMPEGNO<>'Intervento' AND I.URGENTE='T'))
    THEN OUTVAL = 'T';
    ELSE OUTVAL = 'F';

  SUSPEND;
  EXIT;

END^

