CREATE PROCEDURE IMPIANTI_PRESCRIZ_EXISTS (IN_CODICEPRATICA INTEGER, IN_DATAPRATICA TIMESTAMP)
  RETURNS (OUTVAL CHAR(1))
AS
BEGIN

  IF (EXISTS(SELECT P.IDIMPEGNO FROM IMP_PRESCRIZIONI_VIEW P WHERE P.PRATICA=:IN_CODICEPRATICA AND P.DATAPRATICA1=:IN_DATAPRATICA))
    THEN OUTVAL = 'T';
    ELSE OUTVAL = 'F';

  SUSPEND;
  EXIT;

END^

SET TERM ; ^


