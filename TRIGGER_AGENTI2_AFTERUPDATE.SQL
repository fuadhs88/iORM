CREATE TRIGGER AGENTI2_AFTERUPDATE FOR AGENTI2
ACTIVE AFTER UPDATE
AS
BEGIN

  UPDATE CLIENTI A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE PRATICHE A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE PRVORDCL A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE DIBATESTATE A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE IMPEGNI A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE SCADENZ A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE PRIMANOT A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

  UPDATE EXTFILE A SET A.AGENTE = NEW.NOME
  WHERE A.AGENTE = OLD.NOME;

END
