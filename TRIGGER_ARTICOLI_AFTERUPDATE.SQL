CREATE TRIGGER ARTICOLI_AFTERUPDATE FOR ARTICOLI
ACTIVE AFTER UPDATE POSITION 0
AS
BEGIN

  /* Aggiorna il codice articolo anche nella tabella MAGAZZINI */
  IF (NEW.CODICEARTICOLO <> OLD.CODICEARTICOLO) THEN BEGIN
    UPDATE MAGAZZINI M SET M.CODICEARTICOLO = NEW.CODICEARTICOLO WHERE M.CODICEARTICOLO = OLD.CODICEARTICOLO;
  END

END
