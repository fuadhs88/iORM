SELECT RF.RDB$FIELD_NAME, F.RDB$FIELD_TYPE, F.RDB$FIELD_LENGTH, F.RDB$CHARACTER_LENGTH, F.RDB$FIELD_PRECISION,
 F.RDB$FIELD_SCALE, F.RDB$FIELD_SUB_TYPE, F.RDB$NULL_FLAG, F.RDB$COLLATION_ID, F.RDB$CHARACTER_SET_ID,
 RF.RDB$FIELD_POSITION, RF.RDB$NULL_FLAG, RF.RDB$COLLATION_ID
FROM RDB$RELATION_FIELDS RF
LEFT JOIN RDB$FIELDS F ON (F.RDB$FIELD_NAME = RF.RDB$FIELD_SOURCE)
WHERE RF.RDB$RELATION_NAME = 'CLIENTI'
ORDER BY RF.RDB$FIELD_POSITION

